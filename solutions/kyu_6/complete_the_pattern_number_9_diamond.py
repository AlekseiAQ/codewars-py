def make_line(n, i):
    indent = ' ' * (n - i)
    m_line = list(range(1, i+1)) + list(range(i-1, 0, -1))
    m_line = [el % 10 for el in m_line]
    m_line = ''.join(map(str, m_line))
    return indent + m_line + indent


def pattern(n):
    return '\n'.join(make_line(n, i) for j in (range(1, n+1), range(n-1, 0, -1)) for i in j)
